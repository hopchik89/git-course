		 int b=7;
		 System.out.println(a>b); //true
		 System.out.println(a<b); //false
		 System.out.println(a==b); //false
		 System.out.println(a!=b); // !=not equal
		 
		 boolean result=a>=b;
	   	boolean result1=a<=b;
		 System.out.println(result); //true
		 System.out.println(result1); //false
		 
		 int c=20;
		 int d=29;
